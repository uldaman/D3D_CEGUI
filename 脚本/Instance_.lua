	local nowMap = MH_获取当前副本()
	local nowRoom = MH_获取当前房间()

	local pass_map_list, tot = Instance.获取路径(nowMap, tagRoom)
	if pass_map_list ~= nil and tot > 0 then	--匹配到最短路径
		for i = 1, tot - 1 do	--每次for循环就是过一次图, tot为起始图到终点图的总个数 ,tot -1 才是过图的次数
			while nowRoom ~= pass_map_list[tot-i] and MH_是否工作() == 1 do
				--pass_map_list[tot-i] 临时目标地图 pass_map_list[tot-i+1] 临时当前地图
				MH_调试(Instance.MapList[nowMap][pass_map_list[tot-i+1]][pass_map_list[tot-i]].key)
				MH_过图(Instance.MapList[nowMap][pass_map_list[tot-i+1]][pass_map_list[tot-i]].key)
				MH_延时(1000)
				nowRoom = MH_获取当前房间()
			end
		end
		MH_调试("已到达目标地点!")
	end
end

function Instance.S_副本过图(tagRoom)
	local bIsInFB = MH_是否在副本() --2 表示在FB
	local nowRoom

	while bIsInFB == 2 and MH_是否工作() == 1 do
		nowRoom = MH_获取当前房间()

        if nowRoom == tagRoom then
            return
		end

		if nowRoom ~= tagRoom then
			Instance.瞬移到房间(tagRoom)
			MH_延时(1000)
		end

		MH_延时(1000)
		bIsInFB = MH_是否在副本()
	end
end

function Instance.KillMonster(szMonster, nPer)
	local bIsInFB = MH_是否在副本() --2 表示在FB
	local nowRoom, tagRoom

	while bIsInFB == 2 and MH_是否工作() == 1 do
        local 血比 = MH_获取当前血比()
        if 血比 <= nPer then
            MH_开启无敌()
        end

		nowRoom = MH_获取当前房间()
		tagRoom = MH_获取标记怪物房间()

		if tagRoom == 0 then --表示怪物已死
			return
		end

		if nowRoom ~= tagRoom then
			Instance.S_副本过图(tagRoom)
			MH_延时(1000)
		end

		if MH_是否工作() == 0 then
			return
		end

		if nowRoom == tagRoom then
			Instance.瞬移到标记怪()
			MH_延时(250)

			if MH_是否工作() == 0 then
				return
			end

			MH_转向标记怪()
			MH_延时(100)

-- 这里需要按F11 开启面向自动调整
--            MH_前进()
--            MH_延时(250)
--            MH_停止("前进")
--            MH_延时(250)

			if MH_是否工作() == 0 then
				return
			end

			Instance.攻击(szMonster, "身体")
		end

		if MH_是否工作() == 0 then
			return
		end

		MH_延时(1000)
		bIsInFB = MH_是否在副本()
	end
end

local EVENT_KILL_MONSTER = 1000 -- 击杀标记怪

RegisterEvent("EventHandle")

function EventHandle( nId, ... )
    if  nId == EVENT_KILL_MONSTER then
        local arg = table.pack(...)
        Instance.KillMonster(arg[1], 0.5)
        MH_关闭无敌()
    end
end