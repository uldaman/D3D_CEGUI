--根据房间号查找ID, 此ID为计数用, 并非游戏真实ID
function Instance.FindRoomID(mapName, roomID)
	local id = 1
	for i, v in pairs(Instance.MapList[mapName]) do
		if roomID == i then
			break
		end
		id = id + 1
	end
	return id
end

--根据ID序号查找房间号
function Instance.FindRoomName(mapName, k)
	local count_map = 0			--记录地图数量
	for i, v in pairs(Instance.MapList[mapName]) do
		count_map = count_map + 1
	end

	local find_map = 0
	local id = 1
	for i, v in pairs(Instance.MapList[mapName]) do
		if id == k then
			find_map = i
		end
		id = id + 1
	end
	return find_map
end

function Instance.FindBoss(mapName)	--返回途径总地图(包括起始地图和目标地图)列表, 和起始图到终点图的总个数(包括起始地图和目标地图)
	local nowRoom = MH_获取当前房间()
	local tagRoom = MH_获取BOSS房间()

	if nowRoom == tagRoom then --如果在同一个房间
		return nil, 0
	end

	MH_调试("当前房间是 "..nowRoom)
	MH_调试("目标房间是 "..tagRoom)
	--MH_调试(Instance.MapList[mapName][tonumber(nowRoom)][tonumber(tagRoom)].key)

	local count_map = 0	--房间节点总数量
	for i,v in pairs(Instance.MapList[mapName]) do
		count_map = count_map+1
	end
	--MH_调试(count_map)

	--建立好邻接矩阵, 并初始化
	dist = {}		--记录起点到各个点的最短距离
	final = {}		--是否是已经确定过的节点
	prev = {}		--记录当前点的前一个节点

	for i = 1, count_map do		--初始化
		dist[i] = 65535
		final[i] = 0

		local temp_room = Instance.FindRoomName(mapName, i)

		if Instance.MapList[mapName][tonumber(nowRoom)][tonumber(temp_room)]~= nil then
			dist[i] = 1
			prev[i] = nowRoom
		else
			prev[i] = ""
		end
	end

	local ZoneNum = Instance.FindRoomID(mapName, nowRoom)
	dist[ZoneNum] = 0
	final[ZoneNum] = 1

-----------------------------------------------
--现在开始主循环, 每次求得源点到某个节点的最短路径
	for i = 2, count_map do
		local tmp_min = 65535	--临时最小路径
		local tmp_room1 = nowRoom	--临时当前节点
		for j = 1, count_map do
			if final[j] == 0 and dist[j] < tmp_min then
				tmp_min = dist[j]
				tmp_room1 = Instance.FindRoomName(mapName, j)
			end
		end

		final[Instance.FindRoomID(mapName, tmp_room1)] = 1

		if tmp_room1 == tagRoom then	--如果找到目标节点的最短路径就立即返回
			break
		end

		--修正临时当前节点的周边节点的最短路径
		for j = 1, count_map do
			if final[j] == 0 then	--节点未被确定
				local temp_room2 = Instance.FindRoomName(mapName, j)
				if tmp_room1 ~= nil and temp_room2 ~= nil then
					if Instance.MapList[mapName][tonumber(tmp_room1)][tonumber(temp_room2)] ~= nil then
						local newDist = dist[Instance.FindRoomID(mapName, tmp_room1)] + 1
						if newDist < dist[j] then
							dist[j] = newDist
							prev[j] = tmp_room1
						end
					end
				end
			end
		end
	end
--循环完成

	local map_id = Instance.FindRoomID(mapName, tagRoom)

	if dist[map_id] == 65535 then
		MH_调试("两地图不可到达")
		return nil, 0
	end

	MH_调试("需要过 "..dist[map_id].." 次地图")	--要过多少次图

--开始匹配查找最短路径(反向匹配)
	local pass_room_list = {}	--保存着过图节点,包括源点和终点
	local tot = 1	--过图数量,包括源点和终点
	pass_room_list[tot] = tagRoom	--先取出终点, (反向匹配)
	tot = tot + 1

	local tmp_prev = prev[map_id]	--取出目标点的前驱

	while tmp_prev ~= nowRoom do
		pass_room_list[tot] = tmp_prev
		tot = tot + 1
		tmp_prev = prev[Instance.FindRoomID(mapName, tmp_prev)]
	end

	pass_room_list[tot] = nowRoom	--源点

	MH_调试("途径 "..tot.." 张地图[包括起点和终点]")	--过图总数量,包括源点和终点

	-- for i = 1, tot do
	-- 	MH_调试(pass_room_list[tot-i+1])	--所有经过的节点
	-- end

	return pass_room_list, tot
end

