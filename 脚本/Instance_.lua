function Instance.一键刷本(szBossName, szPart, nPer)
	local bIsInFB = MH_是否在副本() --2 表示在FB
	local nowRoom, tagRoom

	while bIsInFB == 2 and MH_是否工作() == 1 do
        local 血比 = MH_获取当前血比()
        if 血比 <= nPer then
            MH_开启无敌()
        end

        if 血比 < 0.5 then
            MH_容错式喝药()
        end

		nowRoom = MH_获取当前房间()
		tagRoom = MH_获取BOSS房间()

		if tagRoom == 0 then --表示BOSS已死
			return
		end

		if nowRoom ~= tagRoom then
			Instance.自动找BOSS()
			MH_延时(1000)
		end

		if MH_是否工作() == 0 then
			return
		end

		if nowRoom == tagRoom then
			Instance.瞬移到BOSS()
			MH_延时(250)

			if MH_是否工作() == 0 then
				return
			end

			MH_转向()
			MH_延时(100)

-- 这里需要按F11 开启面向自动调整
--            MH_前进()
--            MH_延时(250)
--            MH_停止("前进")
--            MH_延时(250)

			if MH_是否工作() == 0 then
				return
			end

			Instance.攻击(szBossName, szPart)
		end

		if MH_是否工作() == 0 then
			return
		end

		MH_延时(1000)
		bIsInFB = MH_是否在副本()
	end
end

