function Instance.自动找BOSS()
	local nowMap = MH_获取当前副本()
	local nowRoom = MH_获取当前房间()

	local pass_map_list, tot = Instance.FindBoss(nowMap)
	if pass_map_list ~= nil and tot > 0 then	--匹配到最短路径
		for i = 1, tot - 1 do	--每次for循环就是过一次图, tot为起始图到终点图的总个数 ,tot -1 才是过图的次数
			while nowRoom ~= pass_map_list[tot-i] and MH_是否工作() == 1 do
				--pass_map_list[tot-i] 临时目标地图 pass_map_list[tot-i+1] 临时当前地图
				MH_调试(Instance.MapList[nowMap][pass_map_list[tot-i+1]][pass_map_list[tot-i]].key)
				MH_过图(Instance.MapList[nowMap][pass_map_list[tot-i+1]][pass_map_list[tot-i]].key)
				MH_延时(1000)
				nowRoom = MH_获取当前房间()
			end
		end
		MH_调试("已到达目标地点!")
	end
end

